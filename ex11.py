# Exemplo de uso de True e False em Python

if True:
    print("Isso é verdadeiro!")
else:   
    print("Isso é falso!")
# Exemplo de uso de False em Python


if True and True:
    print("Ambas as condições são verdadeiras!")
else:   
    print("Pelo menos uma condição é falsa!")

if True or False:
    print("Pelo menos uma condição é verdadeira!")
else:   
    print("Ambas as condições são falsas!")
